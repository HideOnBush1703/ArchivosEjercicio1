    private void botonGuardarContactosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarContactosActionPerformed
        //creando un archivo
        Archivo archivo = new Archivo();
        archivo.crearArchivo();
        
        //obteniendo los datos de las cajas de texto
        String nombre,correo,celular;
        
        nombre=cajaNombre.getText();
        correo=cajaCorreo.getText();
        celular=cajaCelular.getText();
        
        //creamos un objeto persona
        Persona persona=new Persona(nombre,correo,celular);
        
        //escribimos los datos de la persona en el archivo de texto
        archivo.escribirTexto(persona);
        
        cajaNombre.setText("");
        cajaCorreo.setText("");
        cajaCelular.setText("");
        
        
    }//GEN-LAST:event_botonGuardarContactosActionPerformed

